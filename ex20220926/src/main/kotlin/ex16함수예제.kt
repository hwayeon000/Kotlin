fun main() {

    println(sum(10,4))
    println(sub(10,4))
    println(mul(10,4))
    println(div(10,4))

}

// 더하기
fun sum(num1 : Int, num2 : Int) = num1 + num2
// 뺴기
fun sub(num1 : Int, num2 : Int) = num1 - num2
// 곱하기
fun mul(num1 : Int, num2 : Int) = num1 * num2
// 나누기
fun div(num1 : Int, num2 : Int) = num1 / num2

